# Example #1: Arithmetic operators in Python
x = 15
y = 4

# Output: x + y = 19
print(x,id(x),type(x))
print("")
print(y,id(y),type(y))
print("")
print('x + y =',x+y)

# Output: x - y = 11
print('x - y =',x-y)

# Output: x * y = 60
print('x * y =',x*y)

# Output: x / y = 3.75
print('x / y =',x/y)

# Output: x // y = 3
print('x // y =',x//y)

# Output: x ** y = 50625
print('x ** y =',x**y)
